package chapter04;

public class StringTest01 {

	public static void main(String[] args) {
		// c: \ temp
		System.out.println("c:\\temp");
		
		// "hello"
		System.out.println("\"hellow\"");
		// \t: tab
		// \r: carrige return
		// \n: new line
		// \b: beep
		System.out.print("hello\tworld\n");
		System.out.print("hello world");
		
		// '
		char c= '\'';
		System.out.println(c);
	}

}
